<div data-role="page" id="ref_prep_frame-actions">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Frame dicepool &#38; actions</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>Each frame has a dicepool for its actions, starting with 2 white dice representing the basic machine and the pilot&#8217;s senses. You can expand this dicepool with up to 4 systems. No more than 2 systems of any given type can be equipped, but otherwise loadouts are unrestricted. A mobile frame always has several baseline capabilities which are expanded with systems. These capabilities are:</p>
		<ul>
			<li>Defend itself</li>
			<li>Move (navigating around cover)</li>
			<li>Spot a target which is out of cover and within direct-fire or hand-to-hand range</li>
			<li>Attack at hand-to-hand range</li>
		</ul>
		<p>Since dice must be assigned to an action to perform it, a frame with no additional systems must choose which actions to perform. Systems are restricted to specific uses, but provide strength in those areas while also adding additional capabilities to the frame.</p>
	</div>
</div>
