		<h1>Weapon ranges</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>Weapon ranges in MFZ are exclusive to one another and not &#8220;downwards-compatible.&#8221; </p>
		<h2>Range increments:</h2>
		<ul>
			<li>Hand-to-hand: up to 1 unit</li>
			<li>Direct-fire: above 1 unit, up to 1 ruler length</li>
			<li>Artillery: beyond 1 ruler length</li>
		</ul>
		<p>This means that direct-fire cannot be used at hand-to-hand range, just as artillery cannot be used at either hand-to-hand or direct-fire range. Each weapon system only works within its increment. If you are unsure about the ranges between things, you can measure at any time during the game, even out of turn.</p>
		<div class="ref-reminder"><p>Reminder: When you measure, measure from the closest points between target and attacker. When in doubt, the target is in range.</p></div>
 	</div>
</div>
<div data-role="page" id="ref_prep_terrain">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Terrain: cover &amp; stations</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>To play MFZ, build several pieces of terrain. Terrain is split into stations, cover, and other terrain.</p>
		<h2>Stations</h2>
		<p>These objects have distinct properties:</p>
		<ul>
		<li>Must be distinct from cover and other terrain</li>
		<li>Must have a location to put a flag or marker</li>
		<li>Maximum base size is 4 &#215; 4 studs</li>
		</ul>
		<p>For your stations, you can build anything that&#8217;s stationary and valuable to defend or seize. The door panel on a drop ship, a cache of medical supplies, a civilian scientist and family, a research facility&#8217;s data storage drives, a truck with a flat tire and a load of fresh peaches. Otherwise, there are no restrictions for stations.</p>
		<div class="ref-hint"><p>Hint: If you want to use something with a larger base, discuss this with your group. Place it on the field like cover and other terrain during the setup phase and designate a 4&#215;4 stud area that can be used as a station: a doorway for a building or a control terminal, perhaps. The rest of the piece is regular terrain or cover. Keep in mind that cover can be targeted and destroyed, while stations cannot.</p>
		<p>During deployment, any player may claim this as a station instead of placing one when they have opportunity to do so. If no player claims it, use it as normal terrain or cover.</p></div>

		<h2>Cover</h2>
		<p>Any structure on the battlefield is cover if it&#8217;s 3 bricks or more high, except stations. Frames that have not been destroyed may count as cover, too. Even hostile ones!</p>
		<p>A frame counts as being in cover, if:</p>
		<ul>
			<li>It is within 1 unit range of the cover</li>
			<li>The cover is between target and attacker</li>
		</ul>
		<p>Ignore terrain pieces further away than 1 unit of the target, even if they are between the target and attacker.</p>

		<div class="ref-hint"><p>Hint: Because cover is damaged by removing a certain number of pieces, the number of pieces you use (at or above 3 bricks high) determine that cover&#8217;s durability. Cover made from many small pieces will provide protection longer than cover made from a few large pieces.</p></div>

		<div class="ref-note"><h2>Suggested house-rule:</h2>
		<p>Instead of entire terrain pieces, count only actual bricks for the purpose of cover.</p></div>

		<h2>Other terrain</h2>
		<p>Terrain that is not a station and less than 3 bricks in height is no longer relevant. It is not cover and does not require frames to navigate around.</p>
		<p>If you are playing with the additional rules for hazardous terrain, you might designate some for that purpose such as irradiated ground, swamps or perilous plants. Optionally, you can build difficult terrain which does not provide cover, but still requires a movement system to traverse.</p>
	</div>
</div>
<div data-role="page" id="ref_play_overview">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Gameplay: Overview</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>A game of MFZ is separated into three phases: bidding, deployment and combat. These phases flow into each other, with the prior phases influencing the following. A good tactical approach begins before the first shot is fired. Your score in the bidding phase gives you a certain tactical position. You deploy your forces based on your position and tactical goals. The combat phase begins and takes shape from your deployment.</p>
	</div>
</div>
<div data-role="page" id="ref_play_bidding">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Bidding Phase</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>At the beginning of a game, compare all present companies against one another. In general, the player with the smallest, weakest company gets the points advantage and starts the battle on the defensive. The player with the largest, most powerful company gets a points penalty and starts the battle on the offensive.</p>
		<div class="ref-hint"><p>Hint: You&#8217;ll have to guess your opponents forces while you&#8217;re designing your company to get the position you hope for. The pregame  bidding is already part of the tactical calculations!</p></div>
		<p>The most advantageous companies are:</p>
		<ul>
		<li>Slightly smaller and weaker, so that you gain the point advantage for little tactical cost</li>
		<li>Significantly larger and stronger, so that the point disadvantage can be overcome by force</li>
		</ul>
	</div>
</div>
<div data-role="page" id="ref_play_ppa">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Assets &amp; points per asset</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>All frames and stations under your control are assets. Each asset has a point value determined by the relative size and strength of the company you sent into the field.</p>
		<p>Points per asset, PPA, is determined by how a company compares to the others at the beginning of the game. To determine PPA, every company starts with a PPA of 5. From here, apply the following modifiers:</p>
		<table class="ref-table">
			<thead>
				<tr>
					<th>Company has&#8230;</th>
					<th>Effect</th>
				</tr>
			</thead>
			<tbody>
				<tr>
					<td>most frames</td>
					<td>-1 PPA</td>
				</tr>
				<tr>
					<td>most systems</td>
					<td>-1 PPA</td>
				</tr>
				<tr>
					<td>least frames</td>
					<td>+1 PPA</td>
				</tr>
				<tr>
					<td>least systems</td>
					<td>+1 PPA</td>
				</tr>
			</tbody>
		</table>
		<p>All players which meet the criteria adjust their scores.</p>
		<p>You calculate points per asset once at the beginning of the game. Your PPA does not change as the game progresses. Yet, as you play, you&#8217;ll lose mobile frames, and stations will change hands. This changes how many assets you hold, not your points per asset.</p>

		<div class="ref-calc"><p>Score = Assets &#215; PPA</p></div>

		<p>Your score determines your tactical position at the beginning of the game, and your initiative (the order in which players activate frames) throughout the game.</p>

		<div class="ref-note"><p>This app will automatically calculate PPA when Asset Tracking is used.</p></div>

		<h3>Ties for defense</h3>
		<p>When you compare your companies during setup, you might tie for the highest score. When this happens, randomly determine a winner. The winner must decide to:</p>
		<ul>
			<li>add a frame to their company (if possible&#8212;you may not go above the maximum)</li>
			<li>remove a mobile frame from their company (again, if possible)</li>
			<li>force the loser to choose instead</li>
		</ul>

		<p>Compare companies again. That means, recalculate points per asset, recalculate starting scores and then proceed.</p>

		<div class="ref-note"><p>Asset Tracking will randomly choose the winner, but you will still need to manually adjust your companies based on your choices.</p></div>

		<h3>Ties for offense</h3>
		<p>If you tie for the lowest starting score, randomly determine a loser. The loser has to&#8230;</p>
		<ul>
			<li>place the point mobile frame</li>
			<li>go last in tactical order, until score changes</li>
		</ul>

		<p>If you plan to bid lower than the maximum number of frames, be sure to bring a spare mobile frame to the game with you in case of a tie.</p>

		<div class="ref-note"><p>Asset Tracking will automatically resolve ties for offense and determine initiative.</p></div>
	</div>
</div>
<div data-role="page" id="ref_play_tactical-position">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Tactical position</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>There are three tactical positions a player can be assigned at the begiining of a game: Defender, Point Attacker and Secondary Attacker. The score determines which position a player holds:</p>

		<table class="ref-table">
			<thead>
				<tr>
					<th>Player has&#8230;</th>
					<th>Tactical Position</th>
				</tr>
			</thead>
			<tbody>
				<tr>
					<td>&#8230; highest score</td>
					<td>Defender</td>
				</tr>
				<tr>
					<td>&#8230; lowest score</td>
					<td>Point Attacker</td>
				</tr>
				<tr>
					<td>&#8230; neither</td>
					<td>Secondary Attacker</td>
				</tr>
			</tbody>
		</table>
	</div>
</div>
<div data-role="page" id="ref_play_deployment">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Deployment Phase</h1>
	</div>
 	<div role="main" class="ui-content">
		<div class="ref-note"><p>Note: This app contains a deployment guide which is presented when the Asset Tracking section is utilized.</p></div>

		<p>Put your cover and terrain pieces on the playing field, except for stations. Any player can adjust the battlefield layout until everyone is satisfied. This may require compromises. When every player approves the layout of the battlefield, deployment of stations and frames begins.</p>
		<h2>Defense setup</h2>
		<p>The player with the highest starting score places all of their stations. The defender places all stations wherever they like, within 1 ruler length of at least one other station. Everything within 8 units range around these stations is the defensive perimeter. Place two frames  within this defensive perimeter.</p>
		<p>If you manage to position your stations in a way that prohibits legal placement of the attackers, then move your stations so they leave at least one third of the battlefield outside the defensive perimeter.</p>

		<h2>Point Mobile Frame</h2>
		<p>The Point attacker goes next, placing the point mobile frame:</p>
		<ul>
			<li>Out of cover</li>
			<li>Outside the defensive player&#8217;s perimeter</li>
			<li>At 1 ruler length distance of one of the Defender&#8217;s frames, in range for (but at the limit of) direct-fire</li>
		</ul>

		<h2>Attackers&#8217; frames</h2>
		<p>After this, all offensive players alternate, descending by score, frame for frame, until all frames are on the field.</p>
		<p>Frames are placed:</p>
		<ul>
			<li>Outside the defensive perimeter</li>
			<li>Outside direct fire range of any of the Defender&#8217;s frames</li>
			<li>Otherwise, unrestricted</li>
		</ul>

		<h2>Attackers&#8217; stations</h2>
		<p>Once all attacking frames are on the field, attackers alternate again, placing stations. On each of their turn, they place one station, wherever they like.</p>

		<h2>Defender&#8217;s other frames.</h2>
		<p>The defender finishes placing all remaining frames. There are no restrictions, except frames placed outside the perimeter must be in cover.</p>

		<div class="ref-hint"><p>Hint: It is legal for frames and stations to be deployed in places where stations immediately change hands, as long as all other criteria are met. For example, the defender may choose to immediately capture any station placed close enough to cover for a legal deployment. An attacker may choose to place a station in a position that forces another payer to capture it. These bold moves may be legal, but are not necessarily good ideas.</p></div>
	</div>
</div>
<div data-role="page" id="ref_play_combat">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Combat Phase</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>The combat phase is broken up in tactical order and combat order. During the round, you will switch back and forth between tactical order and combat order. Keep a running score for each player, as frames are destroyed and stations seized.</p>
		<div class="ref-calc"><p>Score = number of assets &#215; PPA</p></div>
		<p>As scores change, so does tactical order. The round ends when the last frame has taken its turn. At the end of the round, pick up all marker dice and count down to doomsday.</p>
		<div class="ref-note"><p>The Asset Tracker portion of this app will calculate score and update tactical order automatically.</p></div>
	</div>
</div>
<div data-role="page" id="ref_play_tactical-order">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Tactical order</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>While in tactical order, the player with the highest score goes first.</p>
		<ul>
			<li>Choose one of your frames and take its turn</li>
			<li>If you don&#8217;t want to go, you can pass to the next player in tactical order (next highest score)</li>
			<li>If you are the last player with unactivated frames, you must take a turn</li>
			<li>Every frame has one turn per round</li>
		</ul>
		<p>If all your mobile frames have already taken their turns this round, you have to pass to the next player in tactical order. When a mobile frame takes its turn, resolve it. If a frame attacks another frame that has not taken its turn yet, switch to combat order.</p>
		<p>Return to tactical order as needed, beginning again with the player with the highest score, until all frames have taken a turn. If you lose all of your frames, you automatically pass or end the round. You can still count down the doomsday clock.</p>

		<div class="ref-note"><p>Even if you lose all of your frames, your stations still count toward your score unless they are captured. While extremely difficult, it is technically possible to win the game with no frames remaining.</p></div>
	</div>
</div>
<div data-role="page" id="ref_play_combat-order">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Combat order</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>When a frame attacks a frame that has not already activated, combat order is used. In combat order, the attacking &amp; defending frames&#8217; turns entwine.</p>
		<ul>
			<li><em>Attacker</em> takes the first part of their turn</li>
			<li>When <em>Attacker</em> attacks, their turn pauses</li>
			<li><em>Target</em> frame becomes active</li>
			<li><em>Target</em> names a new target, rolls dicepool, and assigns a defense value</li>
			<li><em>Attacker</em> resolves any hits</li>
			<li><em>Target</em> removes any damage taken</li>
			<li><em>Attacker</em> finishes turn</li>
			<li><em>Target</em> finishes their turn, including movement and own attacks</li>
		</ul>
		<p>This counts as both frame&#8217;s turns for this round. The <em>target</em> may make an attack during their turn. At this point, they become the <em>attacker</em> and the newly attacked frame becomes the <em>target</em>. In this way, combat order continues until all combat is resolved.</p>
		<p>Once all combat is resolved, return to tactical order to continue the round.</p>
		<p>If a target is activated during combat order and destroyed it does not get to move or attack. If it is not destroyed, though, it still gets to use any dice already rolled&#8212;regardless of what systems it may have just lost to damage.</p>
	</div>
</div>
<div data-role="page" id="ref_play_capture-stations">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Capturing &#38; contested stations</h1>
	</div>
 	<div role="main" class="ui-content">
		<h2>Capturing stations</h2>

		<p>At the beginning of a game, all stations are under control of their owners. Even without any guards nearby, they remain under their control,  until an opponent&#8217;s frame captures them.</p>

		<p>At any time and for any reason:</p>
		<ul>
			<li>You gain control of a station when you have a frame within 1 unit of the station <strong>and</strong> no other players have a frame within 1 unit of that station.</li>
			<li>You lose control of a station when any other player has a frame within 1 unit of the station	<strong>and</strong> you do not have a frame within 1 unit of the station.</li>
		</ul>

		<p>Otherwise, stations do not change hands&#8212;even if a player&#8217;s frames are destroyed.</p>

		<p>Recalculate scores immediately when a station is gained or lost.</p>
		<div class="ref-hint"><p>Hint: Stations can change hands multiple times in a round.</p></div>

		<h2>Contested stations</h2>

		<p>In a game with three or more players, it&#8217;s possible for a station to be lost without also being captured. All three conditions need to be met:</p>
		<ol>
			<li>A station&#8217;s owner has a frame within 1 unit of it</li>
			<li>Two or more hostile companies move a frame within the 1-unit capture range</li>
			<li>The station owner&#8217;s frame moves away or is destroyed</li>
		</ol>
		<p>When this happens, the hostiles cannot capture the station. While they are in range, the station is contested by the other hostile. The station is lost by the owner, but nobody can claim control of it until the standoff is resolved. Recalculate scores when it is lost, and the again when the standoff is resolved.</p>
	</div>
</div>
<div data-role="page" id="ref_frame_turn-sequence">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>A Frame&#8217;s Turn</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>A frame is either activated by its controlling player or by coming under attack from another frame. Once activated, go through its turn in sequence.</p>
		<ul>
			<li>Designate a target</li>
			<li>Roll dicepool</li>
			<li>Assign defense</li>
			<li>Move and attack (in either order)</li>
			<li>Spot</li>
		</ul>
	</div>
</div>
<div data-role="page" id="ref_frame_target">
	<div data-role="header" data-add-back-btn="true">
		<h1>Target Designation</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>Name a hostile frame as the target of your attack. You can name no target, if you prefer not to make an attack. Cover can also be targeted, instead of a frame. Stations and your own frames cannot be targeted.</p>
		<p>When you designate a target, you need to declare what range will be used for the attack. You can declare a target that is currently out of range. (You must move your frame within the declared range in order to continue making the attack. If you can&#8217;t move into range first, you forgo the attack.)</p>
	</div>
</div>
<div data-role="page" id="ref_frame_roll-dice">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Roll Dicepool</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>Pick up the dicepool as per a frame&#8217;s loadout and roll them all at once. Assign dice to defense, movement, attack and spotting. You can only assign one die per action, and each die can be assigned only once.</p>
		<p>You can assign white dice to any action, as they are wild. You can look at your dicepool results before you choose where to assign white dice. Using whites is optional but will often be necessary especially as a frame takes damage.</p>
		<p>If you do not have enough dice to fulfill an action, this unassigned action counts as 0. Once all dice are assigned to actions, discard any leftover dice.</p>
		<div class="ref-hint"><p>Hint: This includes dice that cannot be assigned, such as a leftover red die, which cannot be assigned to Defense, even if Defense is unassigned. For example, you will always discard at least one red die when you use an attack system because having an attack system gives you two. However, if you assign a white die to your attack instead, you will discard all red dice.</p></div>

		<h2>Example</h2>
		<p>A frame with the loadout of
		<span class="sys-display"><span data-sys="w">W</span><span data-sys="w">W</span><span data-sys="rh">Rd</span><span data-sys="b">B</span><span data-sys="g">G</span></span> rolls:</p>
		<ul class="roll-display "><li data-die="w6" data-type="w">3</li><li data-die="w6" data-type="w">5</li><li data-die="r6" data-type="r">3</li><li data-die="r6" data-type="r">5</li><li data-die="b6" data-type="b">4</li><li data-die="g6" data-type="g">2</li></ul>

		<p>First, the player must assign defense. The player can choose either the blue <span data-die="b6" data-type="b">4</span> or one of the whites <span data-die="w6" data-type="w">3</span><span data-die="w6" data-type="w">5</span>. The player chooses the <span data-die="b6" data-type="b">4</span>. It is assigned to defense and placed on the table near the frame.</p>

		<p>Next, the player can choose to attack or move. The player chooses to move first, and must choose a die for its movement: the green <span data-die="g6" data-type="g">2</span> or one of the whites <span data-die="w6" data-type="w">3</span><span data-die="w6" data-type="w">5</span>. The player needs to move to get in range of its target, and chooses the <span data-die="w6" data-type="w">5</span>. It can now move up to 5 ruler units. The <span data-die="w6" data-type="w">5</span> has been assigned and cannot be used for additional actions. The green <span data-die="g6" data-type="g">2</span> can be discarded now because it is only usable for movement.</p>

		<p>The player now makes an attack and can choose red <span data-die="r6" data-type="r">3</span> or <span data-die="r6" data-type="r">5</span>, or white <span data-die="w6" data-type="w">3</span>. The player chooses <span data-die="r6" data-type="r">5</span>. The red <span data-die="r6" data-type="r">3</span> can be discarded.</p>

		<p>Finally, the player may choose a spot. The frame does not have a spot (yellow) system, so only the white <span data-die="w6" data-type="w">3</span> is available to be assigned. The player is not required to use it.</p>

		<p>The turn is over, and any remaining dice are now discarded.</p>
	</div>
</div>
<div data-role="page" id="ref_frame_defense">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Assign defense</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>Assign a blue die or a white die to your defense. If you don&#8217;t have any defensive systems, you didn&#8217;t roll any blue dice, so choose a white die or forgo your defense, making it 0 for this round. Place a blue die on the battlefield next to the frame, turned to your defense value. This is this mobile frame&#8217;s defense for the entire round; it won&#8217;t change until next round.</p>
		<p>If you rolled your frame&#8217;s dice because there is attack lined up against your frame, resolve the attack now. Wait for your attacker to finish its turn before you continue yours.</p>
		<div class="ref-hint"><p>Hint: Since a standard d6 does not have a face with a zero, you will not be able to place a die on the field if you assign a 0 defense. You will have to remember that this frame has a defense value already; it does not get to re-roll. You can place a different &#8220;0 defense&#8221; marker on the table if you have to.</p></div>
	</div>
</div>
<div data-role="page" id="ref_frame_move-or-attack">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Move or attack</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>You may choose whether to attack before you move or move before you attack. However, you must make your attack within your declared range. If you declare a range that you are not currently within, you must move into range first or forgo the attack.</p>
	</div>
</div>
<div data-role="page" id="ref_frame_moving">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Moving</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>When you move: Assign a green die or a white die to your movement. If you didn&#8217;t roll any green dice, choose a white die or forgo movement, making it 0. Move a number of ruler units equal to or less than the result of your movement die. If you have any movement systems or the bonus d8 for carrying no ranged weapons, you can pass through cover as though it weren&#8217;t there; otherwise, you have to go around it. Any frame with a movement system can also stand atop of cover.</p>
		<p>Standing on frames is permitted. Frames do not &#8220;carry&#8221; other frames or prohibit them from moving; if the lower frame moves, place the upper frame where the lower frame was standing before.</p>
		<div class="ref-reminder"><p>Reminder: Any structure on the battlefield at least 3 bricks high counts as cover, including frames but excluding stations.</p></div>
	</div>
</div>
<div data-role="page" id="ref_frame_attacking">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Attacking</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>When making your attack, check whether the target you designated is within the range you declared. If not, you forgo the attack. Use your movement to get within range if you can. If it is in range, assign a red die or a white die to your attack. Recall that you may only attack at artillery range with an artillery system and at driect-fire range with either a direct-fire system or by using an SSR. At hand-to-hand range, you may attack without having a system equipped but must assign a white to do so. You can voluntarily abort your attack by choosing to assign no die to it. If you abort the attack, the target does not get activated. If your target is at 1 unit range, you do not need an attack system and can attack using whites.</p>
		<p>If it is within range, resolve your attack now against its standing defense. If the target has no defense die because none was assigned when it had a turn, count Defense as 0. If it doesn&#8217;t have a defense die because it has not been activated yet, switch to combat order. Put your turn on hold until your target has a defense value assigned, then resolve your attack and the rest of your turn.</p>
		<p>If you are attacking cover directly, count its defense as 0. Cover cannot be in cover itself.</p>
		<p>Once you&#8217;ve assigned an attack die and told it to your target, it is too late to abort.</p>
	</div>
</div>
<div data-role="page" id="ref_frame_spotting">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Spotting</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>Assign a yellow die or a white die to your spot. If don&#8217;t have any spotting systems, you may still spot by assigning a white die. Declare the target of your spot, based on your spotting systems. Place a yellow die next to your spotted target, turned to the value of your spot die. If the declared target is already spotted, you can only replace the die if yours would be higher. Otherwise, forgo your spotting. You cannot spot first and follow it up with your own attack. If you attack, you spot after it has resolved. You cannot spot cover or stations.</p>
		<div class="ref-reminder"><p>Reminder: Without spotting systems, the target must be within direct fire or hand to hand range and out of cover. With one system, the target must be within direct fire or hand-to-hand range and may also be in cover. With two systems, any frame may be spotted.</p></div>
	</div>
</div>
<div data-role="page" id="ref_frame_resolve-attacks">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Resolving Attacks</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>If there is a spot on the target, you may choose to add its value directly to your attack value. If you do so, remove that spot die from the field. It doesn&#8217;t matter who placed the spot; you can use anybody&#8217;s spot, and anybody can use yours.</p>
		<div class="ref-hint"><p>Hint: While this may seem odd, there is rarely any effective downside to sharing spots. An opponent using your spot helps your cause by attacking another opponent for you!</p></div>
		<p>Calculate your damage dice.</p>
		<div class="ref-calc"><p>Damage Dice = Spot + Attack - Defense</p></div>
		<h2>Resolving Damage Dice</h2>
		<p>Calculate the number of damage dice using the formula. Roll this many six-sided dice, and check the table to see how damage is applied.</p>
 		<table class="ref-table">
			<thead>
				<tr>
					<th>Range</th>
					<th>Target</th>
					<th>Damages on&#8230;</th>
				</tr>
			</thead>
			<tbody>
				<tr>
					<td>Hand-to-hand</td>
					<td>Frame</td>
					<td>4, 5 &amp; 6</td>
				</tr>
				<tr>
					<td>Direct-fire or artillery</td>
					<td>Frame in cover</td>
					<td>4, 5 damages cover<br>
						6 damages target</td>
				</tr>
				<tr>
					<td>Direct-fire or artillery</td>
					<td>Frame not in cover</td>
					<td>5 &amp; 6</td>
				</tr>
				<tr>
					<td>Direct-fire or artillery</td>
					<td>Frame covered by frame</td>
					<td>5 damages covering frame<br>
						6 damages target</td>
				</tr>
				<tr>
					<td>Any</td>
					<td>Cover</td>
					<td>4, 5 &amp; 6</td>
				</tr>
			</tbody>
		</table>
		<p>Apply damage to the cover first, 4s before 5s. If the cover is destroyed mid-process, treat the target as out of cover. Any remaining 5s blow through and damage the targeted frame. 6s always damage the target.</p>
		<p>Apply damage to a covering frame first. If the covering frame is destroyed mid-process, treat the target as out of cover. Any remaining 5s after the cover frame&#8217;s destruction damage the targeted frame.</p>
		<div class="ref-reminder"><p>Reminder: If the covering mobile frame has two defensive systems it is not damaged by stray shots. A second defensive system allows a frame to provide cover without risking damage!</p></div>
		<p>When targeting cover, every hit removes 6 bricks, attacker&#8217;s choice. Cover cannot be spotted.</p>
		<div class="ref-hint"><p>Hint: You can destroy cover more effectively effectively raze your enemy&#8217;s cover by leaving them with less than 3 brick tall ruins!</p></div>

		<h2>Resolving Damage</h2>
		<p>For each point of damage a frame takes, it loses one of its systems. The owner chooses which system it loses. Pop off a representative part of the frame and drop it on the field. Since that system is gone, it doesn&#8217;t provide any benefits in any future turns. If it has no systems left, damage continues to the frame and eliminates white dice. Once both whites are gone, the frame is destroyed. If the frame is within 1 unit range of a station, its owner can choose to have it ignore 1 damage and instead abandon its position, moving the mobile frame away from the station to just outside of contesting range.</p>
		<div class="ref-hint"><p>Hint: Taking damage doesn&#8217;t affect the dice you&#8217;ve already rolled or your frame&#8217;s capabilities unless the frame is destroyed. They&#8217;re yours to use until the end of this mobile frame&#8217;s turn. For example, if you have two spotting systems and choose to remove one of them, you may still spot any frame on the field this turn. During the next round, you will be limited to spotting nearby frames.</p></div>
		<p>When a frame is destroyed, the owner loses points accordingly&#8212;recalculate the score.</p>
	</div>
</div>
