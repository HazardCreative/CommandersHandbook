<div data-role="page" id="ref_play_doomsday">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Doomsday Clock</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>At the beginning of a game, put down the doomsday counter (often a d12 or d20) and set it to 11. This is the doomsday clock. At the end of every round, the doomsday clock counts down by one. In order from highest current score to lowest, each player chooses to count down the DDC by 1 or to pass. When the DDC reaches 0, the battle ends, and the player with the highest score wins. Ties are possible; the game does not go to overtime or use a secondary method to resolve them&#8212;it simply ends in a tie.</p>

		<div class="ref-hint"><p>Hint: Whoever has the highest score will generally want to advance the DDC, whoever has a lower score typically will not.</p></div>

		<div class="ref-note"><p>The Asset Tracker section of this app contains a Doomsday counter.</p></div>
	</div>
</div>
<div data-role="page" id="ref_play_judgment">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Principle of judgment calls</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>Sometimes, situations are unclear. In that case, favor the outcome which was intended by the action. In detail:</p>
		<ul>
		<li>Defense: If it&#8217;s a close call, consider the defender to be in cover</li>
		<li>Moving: If it&#8217;s a close call, allow the frame to move to that position</li>
		<li>Attack &amp; Spot: If it&#8217;s a close call, consider the target to be within the attacker&#8217;s/spotter&#8217;s desired range.</li>
		</ul>
		<div class="ref-hint"><p>Hint: Frames on the border of a given range may be considered &#8220;in range&#8221; for both ranges. This means that placing a frame there to make an attack may make it easier for the opponent to fire back at you as well.</p></div>
	</div>
</div>
<div data-role="page" id="ref_quick_deployment">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Quick deployment overview</h1>
	</div>
 	<div role="main" class="ui-content">
		<div class="ref-note"><p>Note: This app contains a deployment guide which is presented when the Asset Tracking section is utilized.</p></div>
		<ul>
			<li>Defender places all stations, must be within 1 ruler of at least one other station to establish perimeter</li>
			<li>Defender places 2 frames inside of the defensive perimeter (1 ruler around each station)</li>
			<li>Point Attacker places Point Mobile Frame at the limit of&#8212;but within&#8212;1 ruler of one Defender frame, out of cover</li>
			<li>Attackers place all frames, alternating descending by score, outside the defensive perimeter</li>
			<li>Attackers place all stations, alternating descending by score, outside the defensive perimeter</li>
			<li>Defender places remaining frames. If outside of defensive perimeter, must be in cover</li>
		</ul>
