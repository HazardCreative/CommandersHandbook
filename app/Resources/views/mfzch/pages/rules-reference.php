<div data-role="page" id="ref_optional_split-range">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Split-range weapons</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>With everyone&#8217;s approval, you can declare your weapon systems to be split-range. For purposes of building your frames, a split weapon counts as two half-systems, one of each type. A split hand-to-hand/direct-fire weapon counts as half a hand-to-hand weapon system and half a direct-fire weapon system, for instance. You still aren&#8217;t allowed to have more than 2 systems of each type.</p>
		<p>You only add one red d6 for each half-system. If you use only half-systems to get all the way up to the maximum of 2, you don&#8217;t get the red d8. Instead, you will roll four red d6.</p>
		<p>Examples: A split-range pistol might add one red d6 at direct-fire and one red d6 at hand-to-hand. A split-range scoped assault rifle might add one red d6 at direct-fire range and one red d6 at artillery range.</p>
		<div class="ref-hint"><p>Hint: While this rule is quite commonly used with MFZ players, keep in mind that you sacrifice reliability for flexibility.</p></div>
		<div class="ref-note"><p>Note: Split systems can be enabled in the app settings from main page.</p></div>
	</div>
</div>
<div data-role="page" id="ref_optional_elevation">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Climbing, elevation &amp; falling</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>Before you field your companies, you can agree as a group to allow climbing. Mobile frames can gain benefit from climbing onto structures if they&#8217;re at least 6 bricks high and at least 4 studs in area. When a mobile frame moves, climbing up or down 6 bricks&#8217; height counts the same as moving 1 ruler unit horizontally.</p>
		<div class="ref-note"><p>Note: You need terrain to use this optional rule. Build several pieces that provide elevation.</p></div>
		<p>To climb, a mobile frame has to have at least one appropriate movement system. A mobile frame with no movement systems can climb stairs or a ladder, if it&#8217;s built into the structure. <p>A higher mobile frame is in cover to all lower mobile frames. A lower mobile frame is out of cover to any higher mobile frames, unless it&#8217;s under a roof. Mobile frames at the same elevation determine cover normally. Don&#8217;t consider elevation differences less than 6 bricks. If a mobile frame falls&#8212;for instance if the structure it stood on is destroyed&#8212;it could take damage. Roll 1 damage die for every 3 bricks&#8217; height the frame fell, and use the damage chart for hand-to-hand attacks (4, 5 &#38; 6 damage the frame).</p>
	</div>
</div>
<div data-role="page" id="ref_optional_hazardous">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Hazardous terrain &amp; protection</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>Before you create your companies, you can agree as a group to include hazardous terrain in your upcoming battlefield. When you build your frames, you can give them an appropriate environmental system. It counts against your limit of 4 systems per mobile frame. In order to function effectively in hazardous terrain, a mobile frame needs the appropriate environmental system. Without it, when you roll dice, reduce your white dies by one.</p>
		<p>Examples: When fighting in the vacuum of space on a ship&#8217;s hull, a frame might need magnetized soles. When wading through a swamp with irritating gases, a sealed air system is important. Whilst in subzero conditions, a thermal unit would be advisable.</p>
	</div>
</div>
<div data-role="page" id="ref_optional_per-frame-initiative">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Per-frame initiative</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>With this optional rule, every frame has their own initiative. If a frame has less than four systems, roll 2d20. If it has four systems, roll 1d20. Leave the dice as markers next to the frames.</p>
		<p>When a round starts, tactical order starts with the frame that has the lowest number, and goes through the sequence, up to 20. If a frame has 2d20, you can pick which one to use as soon as your lower number comes up. Once chosen, the initiative remains for the duration of the game. Alternatively, roll again for every new round for even more chaos and unpredictable outcomes.</p>
		<div class="ref-hint"><p>Hint: While this adds considerable amount of time to a round, it opens up for a more varied playing field and tactical options. Keep in mind, though, this favors attackers, that normally have the lowest initiatives. You will also need a large number of d20s to use this rule: up to two for each frame on the field.</p></div>
	</div>
</div>
<div data-role="page" id="ref_optional_drop-white">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Dropping Whites for damage</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>If one of your frames takes damage, you may want to drop a White instead of a system&#8212;either because you do not want to lose your last weapon or foresee that you get more use out of the special effect of that system being present over the versatility of the White.</p>
		<p>Keep in mind that you cannot drop your last White, though&#8212;a frame will still be destroyed when all Whites are gone.</p>
	</div>
</div>
<div data-role="page" id="ref_optional_jettison">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Jettisoning Systems</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>Before you roll your dice, you may remove systems from your own frame. Lay them on the field as if destroyed in battle; these cannot be picked up and used again. This has the effect of damaging your frame but may add the Green d8 to the dicepool when you roll. You may not jettison Whites.</p>
	</div>
