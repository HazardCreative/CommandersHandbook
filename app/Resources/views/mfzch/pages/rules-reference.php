		<h1>Example Attack</h1>
	</div>
 	<div role="main" class="ui-content">
		<p>One of Joshua&#8217;s frames is coming under attack by ranged fire. It has already been spotted by one Sebastian&#8217;s frames and now one of Vincent&#8217;s frames is firing. Luckily, it is in cover.</p>
		<ul>
			<li>Joshua&#8217;s defense: 6</li>
			<li>Vincent&#8217;s attack: 4</li>
			<li>Sebastian&#8217;s spot: 5</li>
			<li>4 + 5 - 6 = 3 hits</li>
			<li>Vincent rolls 1, 4, 5 on damage dice</li>
			<li>The hit chart for a target in cover is used; the 1 has no effect</li>
			<li>The 4 is resolved first, damaging the cover</li>
			<li>Vincent removes 6 bricks, which removes the cover</li>
			<li>The hit chart for a target outside of cover is used</li>
