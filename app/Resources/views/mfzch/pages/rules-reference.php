<div data-role="page" id="ref_quick_turn-order">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Turn order</h1>
	</div>
 	<div role="main" class="ui-content">
 		<p>There are two methods that MFZ:RA uses to determine turn order: Tactical Order and Combat Order.</p>

 		<h2>Tactical Order</h2>
 		<p>The game begins in Tactical Order. The player with the highest score right now goes first. The player may choose one of their frames and take its turn, or pass to the player with the next highest score. A player must choose to activate a frame if there is nobody lower to pass to, and a player passes automatically when all frames are activated.</p>

 		<p>If the activated frame makes an attack against a frame that has not already activated, switch to Combat Order. Otherwise, return to the player with the highest score when the turn completes. Don&#8217;t forget to update scores if necessary before beginning the next turn, as it may change the order.</p>

 		<h2>Combat Order</h2>
		<p>In combat order, the attacking &amp; defending frames&#8217; turns entwine. The frame which is coming under fire must activate now. This frame may, in turn, attack another unactivated frame, creating a chain of activations.</p>

		<p>After assigning an attack value and declaring an attack, the attacker&#8217;s turn pauses. The defending frame begins its turn as far as assigning a defense value, then pauses and the attacker finishes its turn. The defender then continues its turn normally.</p>

		<p>Once all combat is resolved, update scores if necessary and return to Tactical Order.</p>

		<div class="ref-hint"><p>More detail on both order types can be found in the full reference.</p></div>
	</div>
</div>
