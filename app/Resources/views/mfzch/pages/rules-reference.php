		<h3>Sprinting</h3>
		<p>If a frame has neither direct-fire or artillery weapon systems installed, it receives a bonus d8 for its movement, which allows it to traverse cover, too. Sprinting can be considered a benefit of carrying no ammunition. Since Sprinting is not a system, it cannot be destroyed when taking damage. If a frame which has ranged weaponry loses those due to damage, it automatically gains this bonus.</p>

		<div class="ref-hint"><p>Hint: A soldier-configured frame carries 1 defensive system, 1 movement system, 1 spotting system, and 1 direct fire weapon system. Every other mobile frame is a specialist of some sort. When you&#8217;re creating your frames, an easy way to start is to make a soldier configuration and then swap systems in and out to specialize as needed.</p></div>
