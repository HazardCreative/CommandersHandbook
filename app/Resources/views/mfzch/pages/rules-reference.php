<div data-role="page" id="ref_what-is_overview">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Overview</h1>
	</div>
	<div role="main" class="ui-content">
		<p>Mobile Frame Zero: Rapid Attack is a tabletop wargame designed to be played with LEGO robots, with rules that are accessible to a broad spectrum of ages and provide casual fun as well as highpitched, intense battles. With a unique setting that allows for myriads of builds when it comes to Mobile Frames and their companies, MFZ is for the builder, the wargamer and everyone in between. The game was released summer of 2012, but is based on the 2002 game &#8220;Mechaton&#8221;, which was just the crunch of the rules you see here. MFZ has been revised by Joshua A.C. Newman, with the help of Mechaton&#8217;s inventor, Vincent Baker and his son Sebastian Baker. Joshua also got Soren Roberts on board, a LEGO designer and freelance graphics artist who has some repute on Flickr for his microscale spaceships, anime-inspired mecha and other LEGO creations.</p>
		<p>MFZ was made to bring affordable wargaming to the kitchen table, without relying on prebuilt models, allowing for as intricate or simple models as a player likes. With that, MFZ is abstracted quite far, simplifying many elements which are much more elaborate in other wargames. MFZ is also heavily favouring action over inaction. With emphasis on attacking, a timer to keep things fast paced, and an intentional shortage of resources, every decision is meant to be tough. High risk elements combined with strategic sacrifices is what makes MFZ an entertaining game.</p>
		<h2>How the designer describes it:</h2>
		<p>Mobile Frame Zero: Rapid Attack is the tenth anniversary edition of Vincent Baker&#8217;s Mechaton. Build yourself a company of robots called mobile frames out of LEGO, give yourself some objectives to defend, lay out a table with a few friends, and fight! Because, just like a LEGO set, building what&#8217;s on the front of the box is fun, but building your own thing is where it really catches fire. So that&#8217;s what we want you to do! Build robots, play with our setting, sure! If you&#8217;re into VOTOMS, Dougram, Macross, or Gasaraki, then we want to play with you!</p>
	</div>
</div>
<div data-role="page" id="ref_what-is_frames">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Mobile Frames</h1>
	</div>
	<div role="main" class="ui-content">
		<p>In the Solar Calendar, the timeline and universe of MFZ, humanity has surpassed the boundaries of Earth and is traveling deep into space using Transit Gates to cover the distance. The majority of the manual labor is conducted by Mobile Frames, large machines somewhere between exoskeleton and full-blown mecha. They are three to four times as tall as humans and develop six times their lift and carrying capabilities, while retaining human mobility and most of the agility. These mobile frames were sooner, rather than later, integrated into military purposes, and are used as the one-stop solution to anything that requires force on the ground. While the military and paramilitary organizations of the Solar Calendar use combat-specific builds, many rebels or less fortunate mercenaries use repurposed labor frames just the same. This reference will use the term &#8220;frame&#8221; when referring to Mobile Frames, combat-oriented or otherwise.</p>
	</div>
</div>
<div data-role="page" id="ref_what-is_why-lego">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Why LEGO?</h1>
	</div>
	<div role="main" class="ui-content">
		<p>LEGO has multiple advantages for wargames. For example, many people already have LEGO at home that they can use to build mobile frames for MFZ:RA. Additionally, once a frame is built, there is no need to paint it up, reducing the prep time until one can actually play. This is also true if a player wants to try out different compositions&#8212;all it takes is a few different bricks. Since there is also a large international community that builds LEGO robots, there is no shortage of inspiration for new and starting builders.</p>
	</div>
</div>
<div data-role="page" id="ref_what-is_scale">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Scale and size restrictions</h1>
	</div>
	<div role="main" class="ui-content">
		<p>MFZ uses what is called the 7p scale. Essentially, an average human is 7 plates of LEGO tall. That means that anything else modeled should reflect this, so when two forces meet, they look roughly at the same dimensions. 7 plates is two bricks and a plate on top.</p>
		<p>Frames should generally fit inside a box that is 10 studs wide, 10 studs deep and 12 bricks tall. There is some wiggle room for quadrupeds, but it should generally be observed as the maximum. Size creep can quickly become an issue. Since there is a definitive size for humans, this also requires some space where the human can fit inside a frame&#8212;generally, the torso. Otherwise, MFZ has no restrictions on what a frame should look like, what colors or parts to be used, and if you don&#8217;t want to use LEGO, a Gundam model with removable systems is fine, too.</p>
		<p>Using an alternate scale is possible, but you will need to make adjustments. Each player must know what scale will be used before building their frames. The ruler and distance units will need to be adjusted accordingly. The size of the play area needs adjusted accordingly. Building cover needs to be done with the new scale in mind, and you may need to adjust the number of bricks removed each time it is damaged.</p>
	</div>
</div>
<div data-role="page" id="ref_prep_overview">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Preparations: Overview</h1>
	</div>
	<div role="main" class="ui-content">
		<p>To play MFZ, you will need to prepare a few things before you can dive into the fight. Since building is a huge part of the MFZ experience, take your time when building your army. As long as they fit within the scale, anything you can come up with is considered legal. It is necessary to have the individual systems of your frames visible.</p>
		<p>You will need:</p>
		<ul>
			<li>One company of mobile frames per player</li>
			<li>Some terrain pieces, for cover and stations</li>
			<li>One ruler as described in the next section, typically eight units long</li>
			<li>A dice assortment</li>
			<li>A way to track the doomsday clock in 11 steps</li>
		</ul>

		<div class="ref-note"><p>Note: This app contains a die simulator and value tracking system which can be used in place of physical dice. However, using real dice is encouraged. It can also be used to track the doomsday clock.</p></div>
	</div>
</div>
<div data-role="page" id="ref_prep_ruler">
	<div data-role="header" data-add-back-btn="true" data-position="fixed" data-tap-toggle="false">
		<h1>Ruler and units</h1>
	</div>
	<div role="main" class="ui-content">
		<p>By default, you will play with a ruler that is eight units long, and one unit is 5 studs in length, or a 1&#215;4 click hinge brick. Combining eight hinge bricks gives you a ruler that can measure around corners, which is especially useful to plot paths around cover and obstacles. The game area should measure between 4 and 6 ruler lengths diagonally and circular; or square. With hinge bricks, 4 lengths is the equivalent to about 50"/128cm. 6 lengths is the equivalent to about 75"/192cm. As a general rule when measuring between two targets, measure between the closest points of them. Barrel to body part, blade to arm, back to cover, etc.</p>
		<h2>Smaller &#38; larger fields</h2>
		<p>If your playing field is bigger or smaller, adjust accordingly. With a bigger table than 6 ruler lengths, expand the ruler units to match the table size, or tape off the edges of corners of your gaming area to stay within 4 to 6 lengths. If your area is smaller than 4 rulers, reduce the ruler to 6 units length. Weapon ranges behave accordingly.</p>
	</div>
</div>
